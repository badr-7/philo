#include "philo_bonus.h"

int	pars(char **s)
{
	int	i;
	int	j;

	i = 1;
	while (s[i])
	{
		j = 0;
		if (s[i][j] == '\0')
			return (write(2, "ERROR\n", 6), 1);
		while (s[i][j])
		{
			if ((s[i][j] >= 58 && s[i][j] <= 127)
			|| (s[i][j] >= 33 && s[i][j] <= 47))
			{
				write(2, "Error\n", 6);
				return (1);
			}
			j++;
		}
		i++;
	}
	return (0);
}
